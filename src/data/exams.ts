import type { Exam, Question } from '../types/exam';

type QuestionSeed = Omit<Question, 'answers'> & {
  choices: string[];
};

function buildQuestions(questionSeeds: QuestionSeed[]): Question[] {
  return questionSeeds.map(({ choices, ...question }) => ({
    ...question,
    answers: choices.map((text, index) => ({
      id: String.fromCharCode(97 + index),
      text,
    })),
  }));
}

